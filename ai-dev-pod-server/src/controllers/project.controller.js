import { v4 as uuidv4 } from "uuid";
import logger from "../utils/logger.js";
import { callOllama } from "../services/ollama.service.js";
import archiver from "archiver";
import path from "path";
import fs from "fs";

const projectController = {
  createProject: async (req, res) => {
    try {
      const { name, description, prompt } = req.body;
      const projectId = uuidv4();

      logger.info(`📞 Calling Llama3 for project: ${projectId}`);
      
      // Call Llama3 model
      const aiResponse = await callOllama("llama3", prompt || `Create a plan for: ${description}`);
      const cleanAiText = typeof aiResponse?.response === "string" ? aiResponse.response.trim() : "";

      logger.info(`✅ Llama3 response received`);

      res.status(201).json({
        success: true,
        message: "Project created successfully",
        data: {
          id: projectId,
          name: name,
          description: description,
          aiResponse: cleanAiText,
          model: aiResponse?.model || "llama3",
        },
      });
    } catch (error) {
      logger.error(`❌ Error: ${error.message}`);
      res.status(500).json({
        success: false,
        message: "Failed to create project",
      });
    }
  },

  getProjects: (req, res) => {
    try {
      logger.info("Fetching all projects");

      res.status(200).json({
        success: true,
        message: "Projects retrieved successfully",
        data: [],
      });
    } catch (error) {
      logger.error(`Error fetching projects: ${error.message}`);
      res.status(500).json({
        success: false,
        message: "Failed to fetch projects",
      });
    }
  },

  downloadProject: async (req, res) => {
    try {
      const { projectName } = req.params;
      logger.info(`📦 Downloading project: ${projectName}`);

      // Set response headers for zip download
      res.setHeader('Content-Type', 'application/zip');
      res.setHeader('Content-Disposition', `attachment; filename="${projectName}.zip"`);

      // Create archiver instance
      const archive = archiver('zip', {
        zlib: { level: 9 } // Maximum compression
      });

      // Handle archiver errors
      archive.on('error', (err) => {
        logger.error(`Archive error: ${err.message}`);
        throw err;
      });

      // Pipe archive to response
      archive.pipe(res);

      // Add project files to archive
      // For now, create a simple README with project info
      archive.append(`# ${projectName}\n\nProject generated by AI Development Pod\n`, {
        name: 'README.md'
      });

      // Add a sample package.json
      const packageJson = {
        name: projectName,
        version: "1.0.0",
        description: `Project: ${projectName}`,
        main: "index.js",
        scripts: {
          test: "echo \"Error: no test specified\" && exit 1"
        }
      };
      archive.append(JSON.stringify(packageJson, null, 2), {
        name: 'package.json'
      });

      // Finalize the archive
      await archive.finalize();

      logger.info(`✅ Project ${projectName} downloaded successfully`);
    } catch (error) {
      logger.error(`❌ Download error: ${error.message}`);
      if (!res.headersSent) {
        res.status(500).json({
          success: false,
          message: "Failed to download project",
        });
      }
    }
  },
};

export default projectController;